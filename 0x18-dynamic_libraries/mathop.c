#include "mathop.h"

/**
 * add - add two numbers
 * @a: first incoming integer
 * @b: second incoming integer
 * Return: added numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub two numbers
 * @a: first incoming integer
 * @b: second incoming integer
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first incoming integer
 * @b: second incoming integer
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * divi - divides two numbers
 * @a: first incoming integer
 * @b: second incoming integer
 * Return: result
 */
int divi(int a, int b)
{
	return (a / b);
}

/**
 * mod - the remainder of two division operation
 * @a: first incoming integer
 * @b: second incoming integer
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
